#include <stdlib.h>

/**
 * _abs - Calulate the absolute value of a number
 *
 * @int: The number to get absolute value of
 *
 * Description: The function receives a number
 * find and return its absolute value
 *
 * Return: The absolute value
 */
int _abs(int)
{
	int r;

	r = abs(int);
	return (r);
}
